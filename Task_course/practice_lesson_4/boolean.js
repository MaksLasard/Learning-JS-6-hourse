// Задача 1: Меньше или равна 0
const test = true

if(test === true) {
    console.log('Верно!')
} else {
    console.log('Неверно!')
}

test === true ? console.log('Верно!') : console.log('Неверно!')

// Задача 2: Меньше или равна 0
const testOne = false

if(testOne !== true) {
    console.log('Верно!')
} else {
    console.log('Неверно!')
}

testOne !== true ? console.log('Верно!') : console.log('Неверно!')